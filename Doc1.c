//                             ------Datatypes-----

#include <stdio.h>

int main()
{
    int A;
    char B;
    float C;
    double D;

    printf("enter any Integer\n");
    scanf("%d", &A);
    printf("%d\n", A);
    printf("enter any Character\n");
    scanf("%c", &B);
    printf("%c\n", B);
    printf("enter any decimal\n");
    scanf("%f", &C);
    printf("%f\n", C);
    printf("enter any decimal\n");
    scanf("%lf", &D);
    printf("%lf\n", D);
    

    return (0);
}